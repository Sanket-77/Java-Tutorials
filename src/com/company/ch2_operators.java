package com.company;

public class ch2_operators {
    public static void main(String[] args) {

        // modulo operator
        int a = 4;
        int b = 6 % a;
        System.out.println(b);

        int c = 9;
        c += 4;
        System.out.println(c);

        // comparison operator
        System.out.println(6==67);
        System.out.println(46>8);
        System.out.println(56>=56);
        System.out.println(55<=56);

        // logical operator
        System.out.println(64>5 && 66>66);
        System.out.println(64>67 || 65<88);





    }
}
